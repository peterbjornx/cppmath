#include <math.h>

float tsqrt(float i) {
    return sqrtf(i);
}

float tcos(float i) {
    return cosf(i);
}

float tsin(float i) {
    return sinf(i);
}

float ttan(float i) {
    return tanf(i);
}

double tsqrt(double i) {
    return sqrtf(i);
}

double tcos(double i) {
    return cosf(i);
}

double tsin(double i) {
    return sinf(i);
}

double ttan(double i) {
    return tanf(i);
}

long double tsqrt(long double i) {
    return sqrtl(i);
}

long double tcos(long double i) {
    return cosl(i);
}

long double tsin(long double i) {
    return sinl(i);
}

long double ttan(long double i) {
    return tanl(i);
}
